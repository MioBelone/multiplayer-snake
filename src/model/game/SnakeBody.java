package model.game;

public class SnakeBody {

    private int x;
    private int y;
    //private Color color = Color.GREEN;

    public SnakeBody(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int currentX) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int currentY) {
        this.y = y;
    }

}
